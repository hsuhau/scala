package chapter02

object Scala02_DataType {
    def main(args: Array[String]): Unit = {
        // 数据类型
        // scala是完全面向对象的，所以没有基本数据类型
        val age: Int = 20

        // byte, short, int, long, float, double, boolean, char
        val b: Byte = 10
        val s: Short = 10
        val i: Int = 10
        val lon: Long =  10L
        val f: Float = 10F
        val d: Double = 10D
        val bln: Boolean = true
        var ii: Integer = 10
    }
}
